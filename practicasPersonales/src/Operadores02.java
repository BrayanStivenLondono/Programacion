
public class Operadores02 {

	public static void main(String[] args) {
		//O. Aritmeticos (+ - * /)
		int operacion = 3 * 4;
		double resultado = operacion/3.15;
		System.out.println();
		
		//O. ASIGNACION (Permiten asignar un valor a una variable)
		int numero1 = 45;
		int numero2 = numero1 - 10; //numero2=35, numero1=45
		System.out.println();
		numero2 += 3; //35+3
		numero1 -= 5; //45-5
		numero2 *= 4; //35*4
		System.out.println();
		System.out.println();
		
		//O. INCREMENTALES 
		//  ++  incrementa
		//  --  disminuye
		// * 2  duplicar 
		// - 2  decrementar (2)
		// + 11 incrementar (11)
		int num = 1;
		int num2 =2;
		num ++; // num= 1+1
		num2 --;// num2= 2-1
		System.out.println();
		System.out.println();
		
		
		
		
		
		
		

	}

}
